def normalize(text: str) -> str:
    return text.replace("\r", "").replace("\x07", "").strip()


def main() -> None:
    old_to_new = {ORIGINAL[idx]: text for idx, text in REPLACEMENTS.items()}
    replaced: set[str] = set()

    word = win32com.client.DispatchEx("Word.Application")
    word.Visible = False
    word.DisplayAlerts = 0

    try:
        doc = word.Documents.Open(str(DOCX_PATH), False, False)
        try:
            for i in range(1, doc.Paragraphs.Count + 1):
                para = doc.Paragraphs(i)
                text = normalize(para.Range.Text)
                if text in old_to_new and text not in replaced:
                    para.Range.Text = old_to_new[text] + "\r"
                    replaced.add(text)

            for toc in doc.TablesOfContents:
                toc.Update()
            doc.Fields.Update()
            doc.Save()
        finally:
            doc.Close()
    finally:
        word.Quit()

    missing = [idx for idx in REPLACEMENTS if ORIGINAL[idx] not in replaced]
    print(f"replaced={len(replaced)}")
    print(f"missing={missing}")


if __name__ == "__main__":
    main()
